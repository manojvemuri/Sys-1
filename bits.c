/*
 * isTmax - returns 1 if x is the maximum, two's complement number,
 *     and 0 otherwise 
 *   Legal ops: ! ~ & ^ | +
 *   Max ops: 10
 *   Rating: 1
 */
int isTmax(int x) {
  int a,b;
  a=x+1;
  b=x+a;
  //flip a and add to flipped b to give x, to check if it is Tmax, as adding 1 would give a leading 1
  a=!a;
  x=(~b)+a;
  x=!x;
  //gives 1 for true and 0 for false
  return x;
}
