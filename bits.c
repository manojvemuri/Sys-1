/* 
 * copyLSB - set all bits of result to least significant bit of x
 *   Example: copyLSB(5) = 0xFFFFFFFF, copyLSB(6) = 0x00000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int copyLSB(int x) {
       /*confining the last bit*/
       x = x&0x1;
      /*Shifting the last bit*/
       x = x<<31;
      /*using 31bit arithmetic right-shift, copying least significant bit to all positions*/
       x = x>>31;
  return x;
}
/* 
 * rotate4 - Rotate x to the left by 4
 *   Examples: rotate4(0x87654321) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 10
 *   Rating: 2 
 */
int rotate4(int x) {
  int a, b, c;
  //Find the number at 32-4
  c = 32 + ~4;
  //Creating a mask for the end
  b = ~((~0x0) << 4);
  //Make y the bits being shifted
  a = (x >> c) >> 1;
  a = a & b;
  //Make x the rotated bytes without the shifted bits
  x = x << 4;
  //Combine x and y
  return x|a;
}
/* 
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
  // offset is simply bitPosition for bytes, so byte 1 implies bit position 8 
  int nOs = n << 3;
  int mOs = m << 3;
  int nthByte = ((x & (0xFF << nOs) ) >> nOs) & 0xFF;
  int mthByte = ((x & (0xFF << mOs) ) >> mOs) & 0xFF;
  int i,j,ans;
  i = nthByte << mOs;
  j = mthByte << nOs;
  // resetting the  nTh & mth Byte.
  x &= ~((0xFF << nOs) | (0xFF << mOs));
  // finally joining the reset value with mth and nTh bytes
  ans = x | i | j;
  return ans;
}
/* 
 * rotateLeft - Rotate x to the left by n
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateLeft(0x87654321,4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3 
 */
int rotateLeft(int x, int n) {
  int p,q,ans;
  p = ~(~0 << n);
  q = 33+~n;
  q = (x >> q);
  q = p&q;
  ans=q+(x<<n);
  return ans;
}
/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
  int mask, shift,ans;
  //Byte wide mask
  mask = 255; 
  shift = n << 3;
  //shift Byte-wide mask by flipping
  mask = ~(mask << shift);
  // to shift bits of c to nthbyte
  c = c << shift; 
  //to find the nth byte of x, we use mask to x, then we replace the nth byte given
  ans=(x & mask) | c; 
  return ans;
}
